<h4>Doctor's Profile</h4>
<p>Dr. Manicka Saravanan is a reputed Geriatrician with postgraduate training in Geriatric Medicine at the prestigious All India Institute of Medical Sciences (AIIMS) in New Delhi, under the guidance of the renowned Prof. A.B. Dey, who played a pivotal role in establishing Geriatrics as a specialized field in India. Dr. Saravanan brings in close to a decade of clinical and research experience to the organization. </p>

<p>His research interests encompass a wide range of areas including orthogeriatrics, pain and palliative care, geriatric syndromes, and cognitive disorders. He has actively shared his research findings at numerous national and international conferences. Dr. Saravanan has also served as a teaching faculty member for esteemed international and national organizations such as the International Society of Geriatric Oncology (SIOG)-Asia Pacific region, Indian Academy of Geriatrics (IAG), Indira Gandhi National Open University (IGNOU), and the World Health Organization-Integrated care for Older People (WHO-ICOPE) master training. </p>

<p>Dr. Saravanan has a profound belief in the importance of holistic care for the elderly individuals. One of Dr. Saravanan's areas of expertise lies in Comprehensive Geriatric Assessment (CGA). Through CGA, he offers personalized care to address the complex challenges of multimorbidity, polypharmacy, and falls—issues that are more prevalent among the elderly population compared to younger individuals. By utilizing CGA, Dr. Saravanan effectively addresses the unique needs of each patient, providing them with the highest standard of care.</p>

<p>Furthermore, Dr. Saravanan has developed a specialized focus on frailty—an intricate medical condition that affects vulnerable elders, leading to reduced physical strength and resilience. It results in a decline in overall health and physical function. Through his extensive research in frailty, he has gained a deep understanding of the condition. He employs thorough geriatric assessments to identify healthcare indicators of frailty, enabling him to create tailored care plans for vulnerable individuals. Driven by a patient-centered approach, he strives to improve the quality of life for older adults and contribute to the advancement of geriatric medicine as a whole.
</p>
